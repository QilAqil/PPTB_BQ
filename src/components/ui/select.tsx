export interface SelectValueProps {
  placeholder?: string
  children?: React.ReactNode
}

const SelectValue = ({ placeholder, children }: SelectValueProps) => (
  <span className="block truncate">
    {children || placeholder}
  </span>
)
SelectValue.displayName = "SelectValue"

